    @pytest.mark.asyncio
    async def test_multiple_risk_predictions_all_have_signals(self, db):
        """When multiple categories trigger risk predictions, all include supporting_signals.

        Edge case: two categories each exceed the 25%/$200 threshold.  Both
        predictions must carry their own signals with the correct category value.
        """
        engine = _make_engine(db)
        # Both dining and entertainment dominate:
        # dining=$400 (50%), entertainment=$300 (37.5%), transport=$100 (12.5%)
        transactions = [
            _make_transaction_row("dining", 400.0),
            _make_transaction_row("entertainment", 300.0),
            _make_transaction_row("transport", 100.0),
        ]
        # Pad to ≥5 rows so the engine doesn't exit early
        transactions.append(_make_transaction_row("transport", 1.0))
        transactions.append(_make_transaction_row("transport", 1.0))

        with patch.object(engine.db, "get_connection") as mock_ctx:
            mock_conn = MagicMock()
            mock_conn.__enter__ = MagicMock(return_value=mock_conn)
            mock_conn.__exit__ = MagicMock(return_value=False)
            mock_conn.execute.return_value.fetchall.return_value = transactions
            mock_ctx.return_value = mock_conn

            predictions = await engine._check_spending_patterns({})

        risk_preds = [p for p in predictions if p.prediction_type == "risk"]
        assert len(risk_preds) >= 2, (
            "Expected at least 2 risk predictions when both dining and entertainment dominate"
        )

        categories_in_signals = {p.supporting_signals["category"] for p in risk_preds}
        assert "dining" in categories_in_signals, "dining risk prediction must include category"
        assert "entertainment" in categories_in_signals, (
            "entertainment risk prediction must include category"
        )

        # Every prediction must have supporting_signals
        for pred in risk_preds:
            assert pred.supporting_signals is not None
            assert "category" in pred.supporting_signals
            assert "amount" in pred.supporting_signals
            assert "percentage" in pred.supporting_signals


