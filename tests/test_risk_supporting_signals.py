"""
Tests verifying that spending-pattern ``risk`` predictions include structured
``supporting_signals`` so BehavioralAccuracyTracker._infer_risk_accuracy() can
close the learning loop without relying on fragile description parsing.

Background
----------
``_check_spending_patterns()`` was the only prediction generator that did NOT
populate ``supporting_signals``.  The tracker's ``_infer_risk_accuracy()``
method (tracker.py lines 703-705) expects three fields:

* ``category``   — the spending category that was flagged
* ``amount``     — the dollar amount spent in that category
* ``percentage`` — the share of total spending (0-100 scale)

Without these fields the tracker immediately falls back to regex-parsing the
description, which is fragile for category names with special characters and
which also prevented the fast-path from firing, leaving all risk predictions
permanently unresolved.

This module verifies that every risk prediction generated by
``_check_spending_patterns()`` contains the required signals.
"""

import json
from datetime import datetime, timedelta, timezone
from unittest.mock import MagicMock, patch

import pytest

from models.user_model import Prediction


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _make_engine(db):
    """Construct a PredictionEngine with minimal mocked dependencies.

    Uses the same pattern as the other supporting-signals test modules so
    fixtures are compatible.
    """
    from services.prediction_engine.engine import PredictionEngine

    ums = MagicMock()
    ums.store_prediction.return_value = None
    ums.get_signal_profile.return_value = None

    engine = PredictionEngine(db=db, ums=ums)
    return engine


def _make_transaction_row(category: str, amount: float, days_ago: int = 5) -> MagicMock:
    """Return a fake DB row for a ``finance.transaction.new`` event.

    Args:
        category: The spending category (e.g. 'groceries', 'dining').
        amount:   The transaction amount in dollars (positive).
        days_ago: How many days ago the transaction occurred.

    Returns:
        A MagicMock that behaves like a sqlite3.Row when accessed by key.
    """
    ts = (datetime.now(timezone.utc) - timedelta(days=days_ago)).isoformat()
    payload = json.dumps({
        "category": category,
        "amount": -abs(amount),   # Expenses are negative in the source data
        "date": ts,
    })
    row = MagicMock()
    row.__getitem__ = MagicMock(side_effect=lambda k: {
        "payload": payload,
        "timestamp": ts,
    }[k])
    row.get = MagicMock(side_effect=lambda k, d=None: {
        "payload": payload,
        "timestamp": ts,
    }.get(k, d))
    return row


def _build_transactions(category_amounts: dict[str, float]) -> list[MagicMock]:
    """Build a list of transaction rows from a {category: amount} mapping.

    Each amount is split into a single transaction for simplicity.
    """
    rows = []
    for cat, amt in category_amounts.items():
        rows.append(_make_transaction_row(cat, amt))
    return rows


