class TestRiskSignalsTrackerCompatibility:
    """Verify signals match exactly what BehavioralAccuracyTracker._infer_risk_accuracy() reads."""

    @pytest.mark.asyncio
    async def test_signals_are_json_serializable(self, db):
        """Risk supporting_signals must survive json.dumps/json.loads round-trip.

        Signals are stored in the DB as JSON.  If any value is not
        serializable (e.g., a numpy float or datetime), storage silently breaks.
        """
        engine = _make_engine(db)
        transactions = _build_transactions({"groceries": 350.0, "utilities": 80.0})
        while len(transactions) < 5:
            transactions.append(_make_transaction_row("utilities", 5.0))

        with patch.object(engine.db, "get_connection") as mock_ctx:
            mock_conn = MagicMock()
            mock_conn.__enter__ = MagicMock(return_value=mock_conn)
            mock_conn.__exit__ = MagicMock(return_value=False)
            mock_conn.execute.return_value.fetchall.return_value = transactions
            mock_ctx.return_value = mock_conn

            predictions = await engine._check_spending_patterns({})

        risk_preds = [p for p in predictions if p.prediction_type == "risk"]
        assert risk_preds

        for pred in risk_preds:
            # Should not raise
            serialized = json.dumps(pred.supporting_signals)
            recovered = json.loads(serialized)
            assert recovered["category"] == pred.supporting_signals["category"]
            assert abs(recovered["amount"] - pred.supporting_signals["amount"]) < 0.01

    @pytest.mark.asyncio
    async def test_category_field_matches_description(self, db):
        """The 'category' in supporting_signals must match the category in the description.

        Before this fix, the tracker parsed the description with regex to find
        the category (e.g. ``on 'groceries'``).  The signal must be consistent
        with the description so existing regex-fallback paths remain valid.
        """
        engine = _make_engine(db)
        transactions = _build_transactions({"groceries": 450.0, "transport": 80.0})
        while len(transactions) < 5:
            transactions.append(_make_transaction_row("transport", 5.0))

        with patch.object(engine.db, "get_connection") as mock_ctx:
            mock_conn = MagicMock()
            mock_conn.__enter__ = MagicMock(return_value=mock_conn)
            mock_conn.__exit__ = MagicMock(return_value=False)
            mock_conn.execute.return_value.fetchall.return_value = transactions
            mock_ctx.return_value = mock_conn

            predictions = await engine._check_spending_patterns({})

        risk_preds = [p for p in predictions if p.prediction_type == "risk"]
        assert risk_preds

        pred = risk_preds[0]
        category = pred.supporting_signals["category"]
        # The description format is: "Spending alert: $NNN on 'CATEGORY' this month ..."
        assert f"on '{category}'" in pred.description, (
            f"supporting_signals['category']={category!r} not found in description: "
            f"{pred.description!r}"
        )

    def test_prediction_model_stores_risk_signals(self):
        """Prediction model correctly stores dict supporting_signals for risk type."""
        pred = Prediction(
            prediction_type="risk",
            description="Spending alert: $450 on 'groceries' this month (75% of total)",
            confidence=0.5,
            confidence_gate="suggest",
            time_horizon="this_week",
            supporting_signals={
                "category": "groceries",
                "amount": 450.0,
                "percentage": 75.0,
                "total_spending": 600.0,
                "transaction_count": 8,
            },
        )
        assert pred.supporting_signals["category"] == "groceries"
        assert pred.supporting_signals["amount"] == 450.0
        assert pred.supporting_signals["percentage"] == 75.0
        assert pred.supporting_signals["total_spending"] == 600.0
        assert pred.supporting_signals["transaction_count"] == 8

    def test_risk_signals_json_round_trip(self):
        """Risk supporting_signals must survive DB storage round-trip."""
        signals = {
            "category": "dining",
            "amount": 320.50,
            "percentage": 64.1,
            "total_spending": 500.0,
            "transaction_count": 12,
        }
        stored = json.dumps(signals)
        recovered = json.loads(stored)
        assert recovered["category"] == "dining"
        assert abs(recovered["amount"] - 320.50) < 0.01
        assert abs(recovered["percentage"] - 64.1) < 0.01
        assert recovered["transaction_count"] == 12
