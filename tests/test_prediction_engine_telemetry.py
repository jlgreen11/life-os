"""
Tests for prediction engine telemetry and observability improvements.

Validates that the prediction engine emits detailed structured log records
(via the standard ``logging`` module) about prediction generation and filtering,
making the system's decision-making transparent and filterable by log level.

Migration note (iteration 198): print() calls were replaced with logger.debug() /
logger.warning() so that log output respects the configured log level and appears
in structured log capture rather than raw stdout.
"""

import asyncio
import logging
from datetime import datetime, timedelta, timezone

import pytest

from services.prediction_engine.engine import PredictionEngine


@pytest.fixture
def prediction_engine(db, user_model_store):
    """Create a prediction engine instance using conftest fixtures."""
    return PredictionEngine(db, user_model_store)


def test_prediction_engine_logs_generation_stats(db, user_model_store, prediction_engine, caplog):
    """Test that prediction engine emits structured log records about prediction generation.

    The prediction engine should log diagnostic information showing:
    1. How many predictions were generated by each prediction type
    2. The total number of raw predictions
    3. How many predictions were filtered out and why
    4. How many predictions were ultimately surfaced

    Uses caplog (not capsys) because the engine now uses the logging module.
    """
    # Insert a test event to trigger prediction generation
    with db.get_connection("events") as conn:
        conn.execute(
            """INSERT INTO events (id, type, source, timestamp, payload, metadata)
               VALUES (?, ?, ?, ?, ?, ?)""",
            (
                "test-msg-telemetry",
                "email.received",
                "test",
                (datetime.now(timezone.utc) - timedelta(hours=4)).isoformat(),
                '{"from_address": "test@example.com", "subject": "Test", "message_id": "msg-telemetry"}',
                '{"related_contacts": ["test@example.com"]}',
            ),
        )

    with caplog.at_level(logging.DEBUG, logger="services.prediction_engine.engine"):
        loop = asyncio.new_event_loop()
        asyncio.set_event_loop(loop)
        try:
            loop.run_until_complete(prediction_engine.generate_predictions({}))
        finally:
            loop.close()

    # The generation-stats record should appear as a DEBUG log from the engine logger
    gen_records = [r for r in caplog.records if "Generated predictions by type" in r.getMessage()]
    assert len(gen_records) >= 1, "Should emit at least one 'Generated predictions by type' log record"

    # The message should name each prediction type
    msg = gen_records[0].getMessage()
    assert "calendar_conflicts" in msg
    assert "follow_up_needs" in msg
    assert "routine_deviations" in msg
    assert "relationship_maintenance" in msg
    assert "preparation_needs" in msg
    assert "spending_patterns" in msg
    assert "total=" in msg


def test_filtering_stats_logged(db, user_model_store, prediction_engine, caplog):
    """Test that filtering statistics are logged showing why predictions were dropped.

    The engine should log:
    - How many raw predictions were generated
    - How many predictions were ultimately surfaced
    - Counts for reaction-filtered and confidence-filtered predictions

    Uses caplog so the assertion is log-level aware.
    """
    now = datetime.now(timezone.utc) - timedelta(hours=4)
    with db.get_connection("events") as conn:
        for i in range(3):
            conn.execute(
                """INSERT INTO events (id, type, source, timestamp, payload, metadata)
                   VALUES (?, ?, ?, ?, ?, ?)""",
                (
                    f"filter-test-{i}",
                    "email.received",
                    "test",
                    now.isoformat(),
                    f'{{"from_address": "sender{i}@example.com", "subject": "Test {i}", "message_id": "filter-msg-{i}"}}',
                    f'{{"related_contacts": ["sender{i}@example.com"]}}',
                ),
            )

    with caplog.at_level(logging.DEBUG, logger="services.prediction_engine.engine"):
        loop = asyncio.new_event_loop()
        asyncio.set_event_loop(loop)
        try:
            loop.run_until_complete(prediction_engine.generate_predictions({}))
        finally:
            loop.close()

    # Filtering summary record
    filter_records = [r for r in caplog.records if "Filtering:" in r.getMessage()]
    assert len(filter_records) >= 1, "Should emit at least one 'Filtering:' log record"

    msg = filter_records[0].getMessage()
    assert "raw" in msg
    assert "surfaced" in msg


